    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mobileshop_db", "root", "root");
            int row = jTable1.getSelectedRow();
            String table_click = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from purchase where pno='" + table_click + "'";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery(sql);
            if (rs.next()) {
                String add1 = rs.getString("pno");
                pno.setText(add1);
                String add2 = rs.getString("pdate");
                pdate.setText(add2);
                String add3 = rs.getString("sid");
                sid.setSelectedItem(add3);
                String add4 = rs.getString("sname");
                sname.setText(add4);
                String add6 = rs.getString("pname");
                pname.setText(add6);
                String add7 = rs.getString("modno");
                modno.setText(add7);
                String add8 = rs.getString("qty");
                qty.setText(add8);
                String add9 = rs.getString("amt");
                amt.setText(add9);
                String add10 = rs.getString("total");
                total.setText(add10);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

