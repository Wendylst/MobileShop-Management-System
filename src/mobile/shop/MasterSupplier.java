 public void AutoInc() {
        try {
            String num = null;
            int no;
            String sql = "select sid from supplier";
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mobileshop_db", "root", "root");
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                num = (rs.getString("sid"));
            }
            no = Integer.parseInt(num) + 1;
            sid.setText(String.valueOf(no));
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(this, "supplier ID Not Set From Database", "INFORMATION", JOptionPane.ERROR_MESSAGE);
        }
    }  
    

