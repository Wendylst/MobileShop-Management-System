     	public void AutoInc() {
        try {
            String num = null;
            int no;
            String sql = "select pno from purchase";
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mobileshop_db", "root", "root");
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                num = (rs.getString("pno"));
            }
            no = Integer.parseInt(num) + 1;
            pno.setText(String.valueOf(no));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "purchase no Not Set From Database", "INFORMATION", JOptionPane.ERROR_MESSAGE);
        }
        
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mobileshop_db", "root", "root");
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery("select sid from supplier");
            while (rs.next()) {
                sid.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Customer Data Does Not Exit", "INFORMATION", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    

