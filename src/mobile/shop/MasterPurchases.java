                ps1.setString(6, modno.getText());
                ps1.setString(7, qty.getText());
                ps1.setString(8, amt.getText());
                ps1.setString(9, total.getText());
                ps1.execute();
                JOptionPane.showMessageDialog(this, "Record saved successfully");
                updateTable();
                AutoInc();
